package projectSerializeDeserialize;

public class Spouse {

	String name;
	int phNo;
	String company;
	public Spouse(String name, int phNo, String company) {
		super();
		this.name = name;
		this.phNo = phNo;
		this.company = company;
	}
	public String getName() {
		return name;
	}
	public int getPhNo() {
		return phNo;
	}
	public String getCompany() {
		return company;
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
